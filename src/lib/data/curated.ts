type EntityInput = Omit<Entity, "id"> & { id?: string };

function slug(name: string): string {
  return name
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

const B = 1e9;
const M = 1e6;

function e(
  name: string,
  sector: Sector,
  country: string,
  region: Region,
  headquarters: string,
  description: string,
  stats: Partial<Pick<Entity,
    | "founded"
    | "totalFundingUsd"
    | "latestValuationUsd"
    | "marketCapUsd"
    | "revenueUsd"
    | "users"
    | "enterpriseCustomers"
  >>,
  tags: string[] = []
): EntityInput {
  return {
    id: slug(name),
    name,
    sector,
    description,
    headquarters,
    country,
    region,
    tags,
    totalFundingUsd: 0,
    ...stats,
  };
}

